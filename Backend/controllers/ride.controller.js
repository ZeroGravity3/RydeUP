const rideService = require('../services/ride.service');
const { validationResult } = require('express-validator');
const mapService = require('../services/maps.service');
const { sendMessageToSocketId } = require('../socket');
const rideModel = require('../models/ride.model');


module.exports.createRide = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { userId, pickup, destination, vehicleType } = req.body;

    try {
        const ride = await rideService.createRide({ user: req.user._id, pickup, destination, vehicleType });
        // After creating the ride, attempt to notify nearby captains.
        // Do notification in a guarded block so failures here don't cause a 500 after headers were sent.
        try {
            const pickupCoordinates = await mapService.getAddressCoordinate(pickup);

            // map service returns { lat, lng }
            const captainsInRadius = await mapService.getCaptainsInTheRadius(pickupCoordinates.lat, pickupCoordinates.lng, 2);

            // hide otp when broadcasting
            ride.otp = "";

            const rideWithUser = await rideModel.findOne({ _id: ride._id }).populate('user');

           // after you compute captainsInRadius and rideWithUser
captainsInRadius.forEach(captain => {
  console.log("Notifying captain:", captain._id, "socketId:", captain.socketId);
  sendMessageToSocketId(captain.socketId, {
    event: 'new-ride',
    data: rideWithUser
  });
});

        } catch (notifyErr) {
            // Log notification errors but do not fail the request
            console.error('Error notifying captains:', notifyErr.message || notifyErr);
        }

        // Respond to the client after attempting notifications
        res.status(201).json(ride);

    } catch (err) {

        console.log(err);
        return res.status(500).json({ message: err.message });
    }

};

module.exports.getFare = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { pickup, destination } = req.query;

    try {
        const fare = await rideService.getFare(pickup, destination);
        return res.status(200).json(fare);
    } catch (err) {
        return res.status(500).json({ message: err.message });
    }
}

module.exports.confirmRide = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { rideId } = req.body;

    try {
        const ride = await rideService.confirmRide({ rideId, captain: req.captain });

        sendMessageToSocketId(ride.user.socketId, {
            event: 'ride-confirmed',
            data: ride
        })

        return res.status(200).json(ride);
    } catch (err) {

        console.log(err);
        return res.status(500).json({ message: err.message });
    }
}

